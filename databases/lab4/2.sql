EXPLAIN ANALYSE SELECT Н_ЛЮДИ.ИД, Н_ВЕДОМОСТИ.ЧЛВК_ИД, Н_СЕССИЯ.УЧГОД FROM Н_ЛЮДИ
JOIN Н_СЕССИЯ ON Н_ЛЮДИ.ИД = Н_СЕССИЯ.ЧЛВК_ИД
JOIN Н_ВЕДОМОСТИ ON Н_ВЕДОМОСТИ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
WHERE Н_ЛЮДИ.ОТЧЕСТВО < 'Георгиевич' AND Н_ВЕДОМОСТИ.ДАТА < TIMESTAMP '2022-06-08';

CREATE INDEX ИНД_ВЕДОМОСТИ_ДАТА ON Н_ВЕДОМОСТИ USING BTREE("ДАТА");
CREATE INDEX ИНД_ЛЮДИ_ОТЧЕСТВО ON Н_ЛЮДИ USING BTREE("ОТЧЕСТВО");