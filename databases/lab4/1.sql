EXPLAIN ANALYSE SELECT Н_ТИПЫ_ВЕДОМОСТЕЙ.НАИМЕНОВАНИЕ, Н_ВЕДОМОСТИ.ИД FROM Н_ВЕДОМОСТИ
JOIN Н_ТИПЫ_ВЕДОМОСТЕЙ ON Н_ВЕДОМОСТИ.ТВ_ИД = Н_ТИПЫ_ВЕДОМОСТЕЙ.ИД
WHERE Н_ТИПЫ_ВЕДОМОСТЕЙ.ИД > 3 AND 
Н_ВЕДОМОСТИ.ДАТА > TIMESTAMP '1998-01-05' AND Н_ВЕДОМОСТИ.ДАТА > TIMESTAMP '2010-06-18';

CREATE INDEX ИНД_ВЕДОМОСТИ_ДАТА ON Н_ВЕДОМОСТИ USING BTREE("ДАТА");